public class java6 {
	public static void main(String[] args) {
		
	
	int a = 3;
	int fact = 1;

	///factorial number 5! = 5*4*3*2*1
	
	
	for(int i=a;i>=1;i--)
	{
		 fact = fact*i;
	}
	System.out.println(fact);
}
}

